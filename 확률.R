#비복원 층화추출 - 계층을 대표하는 표본 추출
install.packages('sampling')
library(sampling)
data("iris")
x <- strata('Species',size = c(2,2,2),
            method = 'srswor',data = iris)
#주어진 데이터를 토대로 층화임의추출을 수행
getdata(iris,x)
#추출이 수행된 후 데이터 프레임으로부터 결과를 추출

#복원 층화추출 - 계층을 대표하는 표본 추출
x <- strata('Species',size = c(2,2,2),
            method = 'srswr',data = iris)
getdata(iris,x)

#계통추출
install.packages('doBy')
library(doBy)
x <- data.frame(1:100)
sampleBy(~x, frac=.1, data = x,systematic = T)

#종별로 10%씩 추출
sampleBy(~Species,frac = .1,data = iris)

