#표본 추출방법
#표본조사 : 더조은 수강생 500명 대상 사용중인 핸드폰 조사 - 10명 추출 후 조사
#  단순랜덤추출: 모집단 개체에 1~500까지 번호 부여 후 10명 추출, SRS방식, 추출틀 필요
#  계통추출: 기본적으로 SRS방식과 유사 단, '개체수/표본수' 로 계산되는 k값을 계산
#           1 - k의 숫자 중 하나를 선택하고 그 숫자에 k를 더해서 개체를 선택하는 방식
#           k = (500/10) => 50 임의의 숫자 25를 뽑은 경우 -> 25,75,125...
#  층화추출: 모집단을 몇 개의 그룹으로 나누어 표본 추출 예를 들어 모집단을 남/여로 나눔
#           각 계층 별로 번호 부여 (남:300, 여:200) 계층 별 비율계산 - 남:여 = 0.6:0.4
#           따라서, 남자는 6, 여자는 4명을 SRS방식으로 추출
#  군집추출: 모집단을 몇 개의 군집으로 나누어 표본 추출 예를 들어 모집단을 학력수준으로 나눔
#           각 군집 별로 번호 부여(고졸, 대졸, 대학원) 임의의 그룹을 선택하고 번호표 부여
#           후 SRS추출

